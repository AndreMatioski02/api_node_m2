class Service {
  constructor(
    id,
    name,
    description,
    employeeName,
    clientName,
    animalName
  ) {
    this.id = id
    this.name = name
    this.description = description
    this.employeeName = employeeName
    this.clientName = clientName
    this.animalName = animalName
  }
}

module.exports = Service